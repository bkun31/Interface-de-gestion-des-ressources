package exception;

public class DAOException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4769045508233375472L;

	public DAOException() {
		super();
	}

	public DAOException(String message, Throwable cause) {
		super(message, cause);
	}

	public DAOException(String message) {
		super(message);
	}

}
